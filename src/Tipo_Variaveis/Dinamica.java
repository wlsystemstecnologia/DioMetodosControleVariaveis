package Tipo_Variaveis;

public class Dinamica {

    /**
     * inicialização dinamica
     * @param args
     */
    
    public static void main(String[] args) {
        double raudius = 4, height = 5;
        
        //inicialização volume dinamicamente
        
        double volume = 3.1416 * raudius * raudius * height;
        
        System.out.println("Volume é " + volume);
        
        
        
    }
    
}
